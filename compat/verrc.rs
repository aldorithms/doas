#[no_mangle]
pub unsafe extern "C" fn verrc(
    mut eval: libc::c_int,
    mut code: libc::c_int,
    mut fmt: *const libc::c_char,
    mut ap: ::core::ffi::VaList,
) {
    fprintf(stderr, b"%s: \0" as *const u8 as *const libc::c_char, getprogname());
    if !fmt.is_null() {
        vfprintf(stderr, fmt, ap.as_va_list());
        fprintf(stderr, b": \0" as *const u8 as *const libc::c_char);
    }
    fprintf(stderr, b"%s\n\0" as *const u8 as *const libc::c_char, strerror(code));
    exit(eval);
}
